#include <stdio.h>
#include <fcntl.h>
#include <unistd.h>
#include "get_next_line.h"

int	get_next_line(const int fd, char **line);

int		main(void)
{
	int fd = open("test1", O_RDONLY);
	char	*line = NULL;

	get_next_line(fd, &line);
	printf("%s\n", line);
	get_next_line(fd, &line);
	printf("%s\n", line);
	get_next_line(fd, &line);
	printf("%s\n", line);
	close(fd);
}
